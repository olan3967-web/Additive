    t: function(key) {
        const langData = this.translations[this.currentLang] || this.translations['en'];
        return langData[key] || key;
    },
    
    setLanguage: function(lang) {
        if (this.translations[lang]) {
            this.currentLang = lang;
            localStorage.setItem('app_language', lang);
            this.updatePageLanguage();
            return true;
        }
        return false;
    },
    
    init: function() {
        const savedLang = localStorage.getItem('app_language');
        if (savedLang && this.translations[savedLang]) {
            this.currentLang = savedLang;
        }
        this.updatePageLanguage();
    },
    
    updatePageLanguage: function() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const translation = this.t(key);
            if (translation && translation !== key) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    if (el.placeholder !== undefined) el.placeholder = translation;
                } else if (el.tagName === 'BUTTON' && el.value !== undefined) {
                    el.value = translation;
                } else {
                    el.textContent = translation;
                }
            }
        });
        document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: this.currentLang } }));
    }
